	<h3>Book parking online or by phone – both are quick and easy</h3>
	<p>You can book your Gatwick parking through either our secure website or by phoning our call centre. Whichever way you choose you can change or cancel your booking for free (with the exception of our SuperSaver prices).</p>

	<h4>How to book parking at Gatwick Airport</h4>
	<p>To make your business or holiday car park booking, you’ll need to have the following handy:</p>
	<ul>
		<li>Date and times of travel </li>
		<li>The terminal you're flying from (<a href="http://www.gatwickairport.com/flights/terminals/" target="_blank"  onclick="">Find out which terminal you're flying from</a>).</li>
		<li>Your car registration, make and colour.</li>
	</ul>

	<h3 style="padding:20px 0 0 18px!important;">Book online</h3>

	<h4>Enter your travel information</h4>
	<p>To book online, enter your terminal, dates of travel and the approximate times you’ll be entering and leaving the car park in the ‘Quick Quote’ form and then click ‘Check Prices’. </p>

	<h4>Choose your car park</h4>
	<p>You’ll see all available parking options, prices and the time it’ll take to reach your terminal. Choose the option that best suits your needs and click the ‘Book’ button.</p>

	<h4>Confirm and pay</h4>
	<p>After confirming that the details are correct, you will be taken to the ‘Personal and payment details’ page where you’ll enter your personal and credit or debit card details. Once you’ve done this and accepted the terms and conditions, click ‘Confirm and pay’. </p>
	<p>Your online payment will then be processed and you’ll be taken to a confirmation page, where you’ll get a booking reference.  Print out this page and remember to use your booking reference in any communication with us. </p>
	<p>You'll also get an email confirming your booking details, which you'll need to print out and bring with you.</p>

	<h4>Book via telephone</h4>
	<p>Bookings can also be made by telephone at <strong>0844 811 8311</strong>.</p>
	<p>When you phone, one of our team will take you through the booking process and give you the choice to either receive your booking confirmation by email or post.</p>

	<h4>Before you book...</h4>
	<p>If you’d like to find out more about the advantages of pre-booking before you go ahead and reserve your space, go to <a href="prebook.php">Why Book Ahead</a>? </p>
	<p>And for details of our online security procedures visit <a href="online-security.php">Safe Booking Online</a>.</p>
