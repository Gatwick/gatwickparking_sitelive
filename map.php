<div class="pink-box-wide">
	<h3>Directions</h3>
	<p><strong>Find directions and the best route to Gatwick’s official car parks.</strong></p>
	<p><a href="http://www.gatwickairport.com/parking-transport/driving-to-gatwick/aa-route-planner/" target="_blank"  onclick=""><img src="images/button-aa-route-planner.jpg" alt="AA Route Planner" style="height:32px;" /></a></p>
	<p>Gatwick is 28 miles (45km) south of London, linked directly to the M23 at Junction 9 and to the A23 London-Brighton road.</p>
	<p>The M25 is a ten-minute drive away (Junction 7), providing connections to the national road and motorway network.</p>
	<p>If you’re using satnav our postcodes are:</p>
	<ul>
		<li>South Terminal: RH6 0NP</li>
		<li>North Terminal: RH6 0PJ</li>
	</ul>

	<h4>Drop-off and pick-up</h4>
	<p>You can drop-off passengers in the designated areas on the terminal forecourts. However, for security reasons there is a strict no-waiting policy on the forecourt. If you want to come into the terminals you must use one of the short stay car parks.</p>
	<p>If you are picking passengers up, you must use a <a href="short-stay-parking.php">short stay car park</a>.</p>
	<p>Car Park 3 (South Terminal) operates a special tariff of 15 minutes' for £1.</p>

	<h4>Car parking</h4>
	<p>Both short stay car parks are signposted from roads into Gatwick, and are a short walk from the terminals.</p>
	<p>Valet parking operates from Short Stay car park 2, level 2 in South Terminal, and Short Stay car park 5, level 3 in North Terminal.</p>
	<p>Long term parking is located a short coach ride from the terminals.</p>
	<p><a href="https://www.gatwickparking.com/booking/Home.aspx" target="_blank" rel="nofollow" onclick="">Book car parking now</a></p>

	<h4>Car hire</h4>
	<p>Major car hire companies have desks in the car rental offices near the entrance of each terminal.</p>
	<p>Find a great deal on airport car hire by <a href="http://www.gatwickairport.com/parking-transport/car-hire/" target="_blank"  onclick="">booking online with one of our rental partners</a>.</p>

	<h4>Motorbikes</h4>
	<p>Motorcyclists can park free of charge at Gatwick using the dedicated bays in the car parks. Motorbikes must not be left in car parking spaces.</p>
	<p>Motorcycle parking bays can be found in the following locations:</p>
	<p><strong>South Terminal:</strong> Car Park 2 (on each floor).</p>
	<p><strong>North Terminal:</strong> Ground floor of the car park.</p>
	<p>Please note that anchor points are not available, and that for security reasons motorbikes will be removed if not parked in the designated areas.</p>

	<h4>Petrol stations</h4>
	<p>There are 24-hour filling stations at both North and South Terminals.</p>

