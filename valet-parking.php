	<p>Valet parking at Gatwick airport is the quickest and easiest parking option and ideal for those with children or luggage.</p>
	<p>Simply arrive at the designated drop-off point on the Departures forecourt at the pre-arranged time and give your Gatwick airport valet parking booking reference number and car keys to the staff. Your car will be parked for you, so you can go straight into the terminal.</p>
	<p>Your car will be parked in one of our secure airport car parks and returned to the forecourt on your return. Valet parking is available at both North and South Terminals</p>
	<p>At the South Terminal the drop-off point is on level two of the RED short stay car park – just follow the signs to the valet reception. On your return, phone the valet staff from the arrivals hall, and they'll have your car ready for you back at the reception where you originally left it.</p>
	<p>At the North Terminal, the service is operated from level 3 of the short stay car park, from where you both drop-off and pick up your car. Please note that this change means a height restriction of 1.8m (6ft) and a width restriction of 2.4m (7ft 10in).</p>
