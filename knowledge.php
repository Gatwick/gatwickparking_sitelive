<div class="pink-box-wide" style="padding-bottom:0px!important;">
	<h3>Find the answers to your commonly asked questions</h3>

	<div class="show-hide">
		<h4 class="title-clicky">About Gatwick Parking</h4>
		<p><strong>What is Gatwick Parking?</strong><br />
Gatwick Parking is the official website for airport parking at Gatwick Airport and designed specifically for pre-booking car park spaces here. Bookings can either be made through either <a href="index.php" target="_blank">www.gatwickparking.com</a> or <a href="http://www.gatwickairport.com" target="_blank" onclick="">www.gatwickairport.com</a>.</p>

		<h4 class="title-clicky">Booking fees</h4>
		<p><strong>Is there a booking fee?</strong><br />
We do not charge booking or Debit Card fees, there is a small admin fee if you wish to pay by Credit Card.</p>

		<h4 class="title-clicky">Booking in the future</h4>
		<p><strong>How far ahead can I book?</strong><br />
It’s possible to book up to 12 months ahead of your travel date – and once you’ve booked you’re protected from any price changes. What’s more if your travel plans change, you can change or cancel without charge providing that you do so more than 24 hours before your arrival time at the car park.</p>
		<p>Please note that this doesn’t apply to SuperSaver prices which offer bigger discounts and can’t be cancelled or changed.</p>

		<h4 class="title-clicky">Notice period to book</h4>
		<p><strong>Can I book at short notice?</strong><br />
The majority of our car parks require a pre-booking period of at least 24 hours before your arrival time at the car park.</p>

		<h4 class="title-clicky">Car park minimum stays</h4>
		<p><strong>If a car park has a minimum stay, can I park there for a shorter period?</strong><br />
You can book and stay for periods less than the minimum stay detailed – but you will be charged for the whole of the minimum stay period.</p>

		<h4 class="title-clicky">Pricing Policy</h4>
		<p><strong>How are parking charges calculated?</strong><br />
Car parks are priced per 24-hour period or part thereof.<br /><br />
The number of 'days' (24-hour periods) charged for when you pre-book is calculated on the basis of the times you select when making your booking. However, if your ACTUAL entry and exit time changes from that specified in your booking and as a result your actual time in the car park runs into another 24-hour period (over and above that booked), you will be liable for an additional day/s charge at the drive-up tariff.<br /><br />
 All prices shown are in UK pounds and include VAT at the current rate of 20%.</p>

		<h4 class="title-clicky">Drive-up price versus pre-book</h4>
		<p><strong>Why is the drive-up price sometimes less than the Gatwick Parking price?</strong><br />
Some car parks have a minimum stay policy which incurs a minimum charge payable for parking. This means that on occasions, <a href="short-stay-parking.php">if your stay is a short one</a>, for some car parks it might be cheaper to drive up rather than pre-book.</p>

		<h4 class="title-clicky">Booking for blue badge holders</h4>
		<p><strong>How do I book disabled parking?</strong><br />
Please contact our call centre team on 0844 811 8311.</p>

		<h4 class="title-clicky">Payment methods</h4>
		<p><strong>What payment methods are accepted?</strong><br />
		<a href="index.php" target="_blank">Gatwickparking.com</a> accepts the following credit and debit cards: Visa, MasterCard, American Express, Diners Club, Switch and Delta. We don’t accept Solo or Electron cards or cheques.</p>

		<h4 class="title-clicky">Cancellations and amendments</h4>
		<p><strong>What if I need to cancel or change my car park booking after I’ve booked?</strong><br />
You can cancel or change a car park booking up to 24 hours before your time of arrival using the <a href="https://www.gatwickparking.com/booking/ManageBooking.aspx" rel="nofollow" target="_blank" onclick="">Manage my booking</a> page. You’ll get  a full refund as long we receive notification of your cancellation in time. <br /><br />
The only exception to this policy is SuperSaver prices, which offer bigger discounts and can’t be cancelled or amended.</p>

		<h4 class="title-clicky">Online security</h4>
		<p><strong>How do I know my payment details are secure?</strong><br />
This site uses industry-standard SSL encryption software. It protects all your personal information, including credit or debit card number, and your name and address. You’ll know you are in a secure area of the site when a padlock appears in the bottom right hand corner of your web browser – and you’ll be warned when you’re about to leave the secure area. Find out more about <a href="online-security.php">online security</a>.</p>

		<h4 class="title-clicky">Order confirmation</h4>
		<p><strong>How do I know that my order has been placed? </strong><br />
Once you’ve finished the booking process, you’ll receive a confirmation email with a booking reference. You should print this out and bring it with you to the airport. If you don’t receive one, please email us at <a href="mailto:gatwick.customerservice@apcoa.com">gatwick.customerservice@apcoa.com</a></p>

		<h4 class="title-clicky">Arrival at the car park</h4>
		<p><strong>What do I do when I get to the car park?</strong><br />
It varies for each car park, but you can find specific details on the <a href="parking.php">Parking options</a> page under your chosen car park.</p>

		<h4 class="title-clicky">Leaving the car park</h4>
		<p><strong>What happens when I return from my trip?</strong><br />
It varies for each car park, but you can find specific details on the Parking options page under your chosen car park.<br /><br />
In many cases the car park uses number plate recognition so the barriers will recognise that you’ve pre-booked and allow you to enter and exit automatically. <br /><br />
If your car park doesn’t use this equipment, you’ll be asked to present your booking reference number and car park entry ticket at the customer service office (near the car park exit) before leaving, so the staff can validate your ticket. <br /><br />
Don’t use the payment machines in the car park as they won’t recognise your booking and will charge you again at the gate price.</p>

		<h4 class="title-clicky">Lost booking reference numbers</h4>
		<p><strong>What do I do if I lose my booking reference number?</strong><br />
Your booking reference can be found at the top of the confirmation email. If experiencing difficulties, go to the <a href="https://www.gatwickparking.com/booking/ManageBooking.aspx" rel="nofollow" target="_blank" onclick="">Find my booking reference</a> page, where you can enter your postcode and email address to submit a request for an email with your booking reference.</p>

		<h4 class="title-clicky">Bringing a different vehicle</h4>
		<p><strong>What happens if I want to bring a different car than on my original booking?</strong><br />
Log into the <a href="https://www.gatwickparking.com/booking/ManageBooking.aspx" rel="nofollow" target="_blank" onclick="_gaq.push(['_link', this.href]);_gaq.push(['_trackPageview', '/goal/parking/step/link/']);">Manage my booking</a> feature, and click ‘Amend booking’ to the car registration box to change the details.</p>

		<h4 class="title-clicky">Valet parking</h4>
		<p><strong>How does valet parking work?</strong><br />
		<a href="valet-parking.php">Valet parking</a> is the quickest and easiest way to park your car. Simply arrive at the designated drop off point on the Departures forecourt at the pre-arranged time and give your booking reference and car keys to one of the valet parking team. They’ll park your car – and you can make your way into the terminal. </p>

		<h4 class="title-clicky">Courtesy buses</h4>
		<p><strong>Will there be a courtesy bus running early in the morning or late at night?</strong><br />
Most car parks operate a 24-hour transfer service to the terminals, though the frequency varies depending upon time of day. You’ll find details on the <a href="parking.php">Parking options</a> page. </p>
