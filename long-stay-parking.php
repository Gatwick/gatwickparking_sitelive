	<h3 class="smallheading">Long Stay Parking Options</h3>
    
	<p>There are two long stay car parks at Gatwick Airport: Long Stay South car park which is located at the South Terminal and Long Stay North car park for those passengers travelling from the North Terminal.</p>
	<p>Both of these are designed for holidays or longer trips away. They are self-park and have a regular and free courtesy coach service to the terminals.</p>
	<p>Apart from our short stay car parks, these are closest to the terminals. </p>
	<p><span style="font-weight:bold;">Gatwick long stay parking at South Terminal:<br />
	</span>
When departing from the South Terminal, the most popular long stay car parks are Long Stay South Gatwick and Long Stay Plus Gatwick. Long Stay South Gatwick is located adjacent to the South Terminal and is a self-park facility.  Long Stay Plus Gatwick is located very close to the terminal; your car is parked for you and then it is just two to three minutes to the terminal. </p>

<p><span style="font-weight:bold;">Gatwick long stay parking at North Terminal:<br />
</span>
If you are looking for long stay parking  for a trip departing from the North Terminal then Gatwick Long Stay North and Summer Special Gatwick are the main choices. They are situated to the north of the terminal.</p>

